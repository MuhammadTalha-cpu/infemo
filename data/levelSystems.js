/* ── Helper Functions ───────────────────────────────────── */

function getLevelSystem(langCode) {
  var lang = window.getLanguage ? getLanguage(langCode) : null;
  if (!lang) return LevelSystems.CEFR;
  return LevelSystems[lang.levelSystemKey] || LevelSystems.CEFR;
}

function getLevel(langCode, levelId) {
  var system = getLevelSystem(langCode);
  if (!system) return null;
  return (
    system.levels.find(function (l) {
      return l.id === levelId;
    }) || null
  );
}

function getLevelTopics(langCode, levelId) {
  var level = getLevel(langCode, levelId);
  if (!level || !level.topicsKey) return [];
  return window[level.topicsKey] || [];
}
